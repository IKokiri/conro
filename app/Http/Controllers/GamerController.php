    {
        $gamer = new Gamer();
        $gamer->nickname = $request->input('nickname');
        $gamer->save();

        return response()->json($gamer,201);
    }

    /**
     * buscar gamer pelo nome
     */
    function findGamerNick(Request $request){

        $nickname = $request->nickname;

        return Gamer::where(['nickname'=>$nickname])->first();
        
    }

